import { mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'esbuild';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = resolve(__dirname, '..');
const entryPoint = resolve(rootDir, 'api/_share/dashboardShareCardRenderer.jsx');
const outfile = resolve(rootDir, 'api/_generated/dashboardShareCardRenderer.mjs');

await mkdir(dirname(outfile), { recursive: true });

await build({
  entryPoints: [entryPoint],
  outfile,
  bundle: true,
  format: 'esm',
  platform: 'node',
  target: 'node20',
  jsx: 'automatic',
  external: [
    'react',
    'react/jsx-runtime',
    'react-dom/server'
  ],
  legalComments: 'none',
  banner: {
    js: '/* eslint-disable */\n// Generated by scripts/build-dashboard-share-renderer.mjs. Do not edit directly.',
  },
  logLevel: 'info',
});
